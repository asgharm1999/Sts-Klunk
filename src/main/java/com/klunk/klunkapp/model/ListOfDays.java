package com.klunk.klunkapp.model;

public class ListOfDays {
	
	private LogData monday;
	private LogData tuesday;
	private LogData wednesday;
	private LogData thursday;
	private LogData friday;
	
	public LogData getMonday() {
		return monday;
	}
	
	public void setMonday(LogData monday) {
		this.monday = monday;
	}
	
	public LogData getTuesday() {
		return tuesday;
	}
	
	public void setTuesday(LogData tuesday) {
		this.tuesday = tuesday;
	}
	
	public LogData getWednesday() {
		return wednesday;
	}
	
	public void setWednesday(LogData wednesday) {
		this.wednesday = wednesday;
	}
	
	public LogData getThursday() {
		return thursday;
	}
	
	public void setThursday(LogData thursday) {
		this.thursday = thursday;
	}
	
	public LogData getFriday() {
		return friday;
	}
	
	public void setFriday(LogData friday) {
		this.friday = friday;
	}
	
}
